const mongoose = require('mongoose');
const Schema = mongoose.Schema;

//Create schema
// TODO: update this
const CopySchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = Copy = mongoose.model('copy', CopySchema);
